package 기본이론_개념;

public class 기본이론5_자료형 {

	public static void main(String[] args) {
		/*
			자료형
			1. 숫자(정수)
				1) 소수점X
				2) integer
			2. 숫자(실수)
				1) 소수점O
				2) double
			3. 문자 한 개
				1) ''
				2) character
			4. 문자 여러개
				1) ""
				2) String
			5. 참과 거짓
				1) true false
				2) boolean
		*/
		System.out.println(10);
		System.out.println(3.14);
		System.out.println('A');
		System.out.println("hello");
		System.out.println(true);

	}

}
