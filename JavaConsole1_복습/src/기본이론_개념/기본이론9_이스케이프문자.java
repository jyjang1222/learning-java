package 기본이론_개념;

public class 기본이론9_이스케이프문자 {

	public static void main(String[] args) {
	/*
		이스케이프 문자
		1. \알파벳
		2. 종류
			\n = new line
			\t = tab
			\"
			\'
	*/
		System.out.println("안녕\n하세요");
		System.out.println("안녕\t하세요");
		System.out.println("\"안녕\"");
		System.out.println("\'안녕\'");
	}

}
